# TYP BOOLEAN
x = True
y = False

# OPERATORY PORÓWNANIA
print( x == y ) # JEST RÓWNY
print( x != y ) # JEST RÓŻNY
print( x >  y ) # JEST WIĘKSZY
print( x <  y ) # JEST MNIEJSZY
print( x >= y ) # JEST WIĘKSZY LUB RÓWNY
print( x <= y ) # JEST MNIEJSZY LUB RÓWNY

# INSTUKCJE WARUNKOWE (IF ELSE ELIF)
if x == y:
    print('x is equal to y')
elif x != y:
    print('x is not equal to y')
else:
    print('error')